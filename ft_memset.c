#include "libft.h"

void	*ft_memset(void *str, int x, size_t l)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = (char*)str;
	while (0 < l--)
		ptr[i++] = x;
	return (ptr);
}
