#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int i;

	i = 0;
	if (n >= 0)
		i = n;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		i = n * (-1);
	}
	if (i >= 10)
		ft_putnbr_fd(i / 10, fd);
	ft_putchar_fd(i % 10 + '0', fd);
}
